#include<stdio.h>
#include<math.h>
int main(){
    int a=10,b=5;
    
    printf("the sum of a and b will be %d\n",a+b);
    printf("the difference of a nd b will be %d\n",a-b);
    printf("the product of a and b will be%d\n",a*b);
    printf("division between a and b will be%d\n",a/b);
    printf("the value of 10 to the power will be%f\n",pow(10,5));


    printf("the sum of 4 and 5 will be%d\n",4+5);
    printf("the sum of 4 and 5.5 will be%f\n",4+5.5);
    printf("the sum of 4.4 and 5.5 will be%f\n",4.4+5.5);
    printf("the result will be %f\n",3.0/9);
  
    return 0;
}